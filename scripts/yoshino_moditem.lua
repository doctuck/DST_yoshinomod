local function withDynamicContext(tbl)
    return setmetatable(tbl, {
        __index = function(t, k)
            if k == "context" then
                -- 收集所有以 "line" 开头的键，并按数字后缀排序
                local lines = {}
                for key, val in pairs(t) do
                    if type(key) == "string" then
                        local num = key:match("^line(%d+)$")
                        if num then
                            lines[#lines + 1] = { num = tonumber(num), val = val }
                        end
                    end
                end
                table.sort(lines, function(a, b) return a.num < b.num end)
                -- 将所有 line 内容拼接，中间用换行分隔（可根据需要修改）
                local parts = {}
                for _, item in ipairs(lines) do
                    parts[#parts + 1] = item.val
                end
                return table.concat(parts, "\n")
            end
            return nil  -- 其他未定义的键返回 nil
        end
    })
end


